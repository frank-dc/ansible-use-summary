package org.frank.statement.order;
/*
    顺序结构语句是程序中最简单最基本的流程控制，没有特定的语法结构，按照代码的先后顺序依次执行，
    程序中大多数代码都是这样执行的。

    总结：写在前面的先执行，写在后面的后执行。
 */
public class OrderDemo {
    public static void main(String[] args) {
        System.out.println("先挣一个亿");
        System.out.println("作为小目标");
        System.out.println("我要暴富");
    }
}
