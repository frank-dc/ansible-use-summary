package org.frank.reference.lei_class.multi_files_class;

public class Student {
    // 成员变量
    // 学号
    String sid;

    // 姓名
    String name;

    // 年龄
    int age;

    // 成员方法
    // 学习的方法
    public void study() {
        System.out.println("学生爱学习");
    }

    // 吃法的方法
    public void eat() {
        System.out.println("学生要吃饭");
    }

    // 睡觉的方法
    public void sleep() {
        System.out.println("学生要睡觉");
    }
}
